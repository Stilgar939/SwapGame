package edu.stilgar.project02;

public class imagenes {
    private String valor;
    private String nom;

    public imagenes() {
    }

    public imagenes(String valor, String nom) {
        this.valor = valor;
        this.nom = nom;
    }

    public String getValor() {
        return valor;
    }

    public String getNom() {
        return nom;
    }
}
